#!/bin/bash
############################################################
# Скрипт предназначен для предоставления удаленного доступа к данному серверу
source global.sh
remote_server="$1"
command="$2"
log "begin"

############################################################
# поднимаем подключение
while [ true ]; do
    log "connect $remote_server"
    $command
    log "disconnect $remote_server"
    sleep 30;
done

############################################################
log "end"