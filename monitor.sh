if [[ -z "$1" ]]; then
    echo sttt | iftop -n -N -i eth3 -m 18M -f "net 10.0.0.0/24 or net 192.168.10.0/24 or proto gre or port 3128";
else if [[ "$1" == "log" || "$1" == "l" ]]; then
    tail -c 10000 -f "$log_file";
fi fi

